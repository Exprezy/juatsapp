    public void cargarValores(){
        txtNombre.setText(usuario.getNombre());
        if(usuario.getSexo().equalsIgnoreCase("MASCULINO")){
            rbMasculino.setSelected(true);
        }
        if(usuario.getSexo().equalsIgnoreCase("FEMENINO")){
            rbFemenino.setSelected(true);
        }
        if(usuario.getSexo().equalsIgnoreCase("OTRO")){
            rbOtro.setSelected(true);
        }
        dpFechaNacimiento.setDate(usuario.getFechaNacimiento());
        
        txtCalle.setText(usuario.getDireccion().getCalle());
        txtColonia.setText(usuario.getDireccion().getColonia());
        txtNumero.setText(usuario.getDireccion().getNumero());
        txtCP.setText(usuario.getDireccion().getCp());
        txtCiudad.setText(usuario.getDireccion().getCiudad());
        txtEstado.setText(usuario.getDireccion().getEstado());
    }
    
