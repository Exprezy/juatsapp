//                && txtCalle.getText().equalsIgnoreCase(usuario.getDireccion().getCalle()) && txtColonia.getText().equalsIgnoreCase(usuario.getDireccion().getColonia())
//                && txtCP.getText().equalsIgnoreCase(usuario.getDireccion().getCp()) && txtCiudad.getText().equalsIgnoreCase(usuario.getDireccion().getCiudad())
//                && txtEstado.getText().equalsIgnoreCase(usuario.getDireccion().getEstado()) && txtNumero.getText().equalsIgnoreCase(usuario.getDireccion().getNumero())
//                && usuario) {
//
//            JOptionPane.showMessageDialog(null, "No ha realizado ningún cambio", "Error", JOptionPane.ERROR_MESSAGE);
//            return;
//        }

            
        Direccion direccion = new Direccion(txtCalle.getText(), txtNumero.getText(), txtColonia.getText(), txtCP.getText(), txtCiudad.getText(), txtEstado.getText());

        Usuario usu = new Usuario();
        usu.setId(usuario.getId());
        usu.setNombre(txtNombre.getText());
        usu.setContraseña(this.usuario.getContraseña());
        usu.setTelefono(usuario.getTelefono());
        usu.setDireccion(direccion);
        usu.setFechaNacimiento(dpFechaNacimiento.getDate());
        usu.setSexo(sexo);
        actualizarFecha=usu;
        this.usuario=usu;
        negocio.actualizar(usu);
