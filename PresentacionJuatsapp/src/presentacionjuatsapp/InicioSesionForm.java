    private void txtCelularIniciarSesionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCelularIniciarSesionKeyTyped
        // TODO add your handling code here:
        if (txtCelularIniciarSesion.getText().length() >= 10) {
            evt.consume();
        }
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;

        if (!numeros) {
            evt.consume();
        }

        if (txtCelularIniciarSesion.getText().trim().length() == 10) {
            evt.consume();
        }

    }//GEN-LAST:event_txtCelularIniciarSesionKeyTyped

