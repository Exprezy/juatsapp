    public void iniciarSesion(){
        if(!validaciones()){
            return;
        }
        
        List<Usuario> lis = negocio.consultarTodos();
        if (lis != null) {
            for (Usuario usuario : lis) {
                String passwordHashed = String.valueOf(txtPasswordInicioSesion.getPassword());
                String password = getSHA256(passwordHashed);
                if (txtCelularIniciarSesion.getText().equalsIgnoreCase(usuario.getTelefono())
                       && password.equalsIgnoreCase(usuario.getContrase침a()) ) {
                    dispose();
                    JOptionPane.showMessageDialog(this, "춰Has ingresado correctamente!", "Informacion", 
                            JOptionPane.INFORMATION_MESSAGE);
                    new ChatForm(usuario);
                    return;
                }
            }
        }
        
        JOptionPane.showMessageDialog(null, "Usuario o Contrase침a incorrectas", "Informacion", JOptionPane.INFORMATION_MESSAGE);
    }
    
